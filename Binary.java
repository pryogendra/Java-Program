class Binary
{
    public static void main(String args[])
    {
        int num1= 10;
        int num2=12;
        System.out.println("Num1 ="+Integer.toBinaryString(num1));
        System.out.println("Num2 ="+Integer.toBinaryString(num2));
        
        int add=num1+num2;
        System.out.print("Addition ="+Integer.toBinaryString(add));
    }
}